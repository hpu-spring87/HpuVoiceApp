public class UserInfoBean {
    public String userName;
    public String userTips;
    public String userMoble;
    public String userLocal;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserTips() {
        return userTips;
    }

    public void setUserTips(String userTips) {
        this.userTips = userTips;
    }

    public String getUserMoble() {
        return userMoble;
    }

    public void setUserMoble(String userMoble) {
        this.userMoble = userMoble;
    }

    public String getUserLocal() {
        return userLocal;
    }

    public void setUserLocal(String userLocal) {
        this.userLocal = userLocal;
    }
}
